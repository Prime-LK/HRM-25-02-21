package com.navitsa.hrm.report;

public class PaySheetBeen {

	String deptment;
	String deducttype;
	double amount;	
	String adddedmethod;
	String total;
	String empname;
	String empid;
	
	
	public String getDeptment() {
		return deptment;
	}
	public void setDeptment(String deptment) {
		this.deptment = deptment;
	}
	public String getDeducttype() {
		return deducttype;
	}
	public void setDeducttype(String deducttype) {
		this.deducttype = deducttype;
	}
	public double getAmount() {
		return amount;
	}
	public void setAmount(double amount) {
		this.amount = amount;
	}
	public String getAdddedmethod() {
		return adddedmethod;
	}
	public void setAdddedmethod(String adddedmethod) {
		this.adddedmethod = adddedmethod;
	}
	public String getTotal() {
		return total;
	}
	public void setTotal(String total) {
		this.total = total;
	}
	public String getEmpname() {
		return empname;
	}
	public void setEmpname(String empname) {
		this.empname = empname;
	}
	public String getEmpid() {
		return empid;
	}
	public void setEmpid(String empid) {
		this.empid = empid;
	}
	
	
}
